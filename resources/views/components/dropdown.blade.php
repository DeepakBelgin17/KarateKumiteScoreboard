$(document).ready(function () {
  init();

  function init() {
    loadStates();
    loadPatients();
    setupEventListeners();
  }

  function populateDropdown(selector, data, placeholder = "Select") {
    let options = `<option value="">${placeholder}</option>`;
    data.forEach(item => {
      const value = item.id || item.name;
      const label = item.name;
      options += `<option value="${value}">${label}</option>`;
    });
    $(selector).html(options);
  }

  function loadStates() {
    $.getJSON('ajax/get_states.php')
      .done(data => populateDropdown('#state', data, "Select State"))
      .fail(showAjaxError("states"));
  }

  function loadDistricts(stateId) {
    $.getJSON('ajax/get_districts.php', { state_id: stateId })
      .done(response => {
        if (response.success) {
          populateDropdown('#district', response.districts, "Select District");
        } else {
          console.error("Districts load error:", response.message);
          $('#district').html('<option value="">Error loading districts</option>');
        }
      })
      .fail(showAjaxError("districts"));
  }

  function showAjaxError(item) {
    return (jqXHR, textStatus, errorThrown) =>
      console.error(`Error loading ${item}:`, textStatus, errorThrown);
  }

  function loadPatients() {
    $.get('ajax/fetch_patients.php')
      .done(data => {
        const isEmpty = data.trim() === "" || data.includes("<!-- empty -->");
        $("#recordsSection").toggle(!isEmpty);
        if (!isEmpty) $('#patientTable').html(data);
      })
      .fail(showAjaxError("patients"));
  }

  function setupEventListeners() {
    $('#state').change(function () {
      const stateId = $(this).val();
      if (stateId) loadDistricts(stateId);
      else $('#district').html('<option value="">Select District</option>');
    });

    $('#patientForm').on('submit', function (e) {
      e.preventDefault();
      if ($('#mobile').val().length !== 10) return alert('Enter a valid 10-digit mobile number');

      $.post('ajax/add_update_patient.php', $(this).serialize())
        .done(resp => {
          alert(resp);
          resetForm();
          loadPatients();
        })
        .fail(showAjaxError("saving patient"));
    });

    $('#clearBtn').click(resetForm);

    $('#mobile').on('input', function () {
      this.value = this.value.replace(/\D/g, '').slice(0, 10);
    });

    $('#door').on('input', function () {
      this.value = this.value.replace(/\D/g, '');
    });

    $(document).on('click', '.edit-btn', handleEdit);
    $(document).on('click', '.delete-btn', handleDelete);
  }

  function resetForm() {
    $('#patientForm')[0].reset();
    $('#id').val('');
    $('#submitBtn').text('Save');
    $('#district').html('<option value="">Select District</option>');
    $('input[name=gender]').prop('checked', false);
    $('#state').val('');
  }

  function handleEdit() {
    const id = $(this).data('id');
    $.get('ajax/fetch_patients.php', { id })
      .done(resp => {
        const data = JSON.parse(resp);

        $('#salutation').val(data.salutation);
        $('#name').val(data.name);
        $(`input[name=gender][value="${data.gender}"]`).prop('checked', true);
        $('#dob').val(data.dob);
        $('#email').val(data.email);
        $('#mobile').val(data.mobile);
        $('#door').val(data.door);
        $('#street').val(data.street);
        $('#area').val(data.area);
        $('#id').val(data.id);
        $('#submitBtn').text('Update');

        // Set state and wait for districts to populate
        $.getJSON('ajax/get_states.php').done(states => {
          populateDropdown('#state', states, "Select State");
          $('#state').val(data.state).trigger('change');

          const waitForDistrict = (attempts = 0) => {
            if (attempts > 10) return;
            if ($('#district option[value="' + data.district + '"]').length > 0) {
              $('#district').val(data.district);
            } else {
              setTimeout(() => waitForDistrict(attempts + 1), 100);
            }
          };

          setTimeout(waitForDistrict, 100);
        });
      })
      .fail(showAjaxError("fetching patient data"));
  }

  function handleDelete() {
    const id = $(this).data('id');
    if (confirm("Are you sure you want to delete this patient record?")) {
      $.post('ajax/delete_patient.php', { id })
        .done(resp => {
          alert(resp);
          loadPatients();
        })
        .fail(showAjaxError("deleting patient"));
    }
  }
});
